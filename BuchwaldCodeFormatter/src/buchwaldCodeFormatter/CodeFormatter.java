package buchwaldCodeFormatter;

public class CodeFormatter {
	public CodeClass codeClass;
	
	// Constructor
	public CodeFormatter() {
		super();
	}
	
	// Methods
	public String formatProperties(String properties, int number) {
		
		return properties;
	}
	
	public String formatConstructors(String constructors, int number) {
		
		return constructors;
	}
	
	public String formatMethods(String methods, int number) {
		
		return methods;
	}
	
	public String formatGettersSetters(String getterSetters, int number) {
		
		return getterSetters;
	}
	
	public String formatToStrings(String toString, int number) {
		
		return toString;
	}
}
